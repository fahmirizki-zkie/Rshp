<h2>Rawat Inap</h2>
<p>
  Rawat inap dilakukan pada pasien-pasien yang parah dan membutuhkan perawatan intensif. 
  Pasien akan diobservasi dan mendapat perawatan intensif di bawah pengawasan dokter dan paramedis yang handal. 
  Sebelum rawat inap, klien wajib mengisi inform konsen, yaitu pernyataan bahwa klien telah diberi penjelasan detail tentang 
  kondisi penyakit pasien serta menyetujui rencana terapi. 
  Klien juga diberitahu biaya layanan. RSHP menerima pembayaran tunai maupun kartu debit bank.
</p>

