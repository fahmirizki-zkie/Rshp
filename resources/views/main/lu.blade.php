<h2>Poliklinik</h2>
<p>
  Poliklinik adalah layanan rawat jalan dimana pelayanan kesehatan hewan dilakukan tanpa pasien menginap. 
  Poliklinik melayani tindakan observasi, diagnosis, pengobatan, rehabilitasi medik, serta pelayanan kesehatan 
  lainnya seperti permintaan surat keterangan sehat. 
  Tindakan observasi dan diagnosis juga bisa diteguhkan dengan berbagai macam pemeriksaan yang bisa kami lakukan, 
  misalnya pemeriksaan sitologi, dermatologi, hematologi, atau pemeriksaan radiologi, ultrasonografi, bahkan pemeriksaan elektrokardiografi. 
  Bilamana diperlukan pemeriksaan lain seperti pemeriksaan kultur bakteri atau pemeriksaan jaringan/histopatologi, 
  kami bekerja sama dengan Fakultas Kedokteran Hewan Universitas Airlangga. 
  Selain itu, kami juga mempunyai rapid test untuk pemeriksaan cepat penyakit-penyakit berbahaya pada 
  kucing (panleukopenia, calicivirus, rhinotracheitis, FIP) dan anjing (parvovirus, canine distemper).
</p>

